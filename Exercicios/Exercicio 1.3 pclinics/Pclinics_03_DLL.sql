USE PCLINICS_TARDE;
GO

CREATE TABLE VETERINARIO(
idVeterinario SMALLINT PRIMARY KEY IDENTITY(1,1),
nomeVet VARCHAR(15) NOT NULL
);
GO

CREATE TABLE DONO(
idDono SMALLINT PRIMARY KEY IDENTITY(1,1),
nomeDONO VARCHAR(15) NOT NULL
);
GO

CREATE TABLE TIPOPET(
idTipoPet SMALLINT PRIMARY KEY IDENTITY(1,1),
nomeTPet VARCHAR(15) NOT NULL
);
GO

CREATE TABLE CLINICA(
idClinica SMALLINT PRIMARY KEY IDENTITY(1,1),
nomeClinica VARCHAR(15) NOT NULL,
End_Clinica VARCHAR(100) NOT NULL
);
GO



CREATE TABLE RAÇA(
idRaça SMALLINT PRIMARY KEY IDENTITY(1,1),
idTipoPet SMALLINT FOREIGN KEY REFERENCES TIPOPET(idTipoPet),
nomeRaça VARCHAR(15) NOT NULL
);
GO

CREATE TABLE PET(
idPet SMALLINT PRIMARY KEY IDENTITY(1,1),
idRaça SMALLINT FOREIGN KEY REFERENCES RAÇA(idRaça),
idDono SMALLINT FOREIGN KEY REFERENCES DONO(idDono),
nomeRaça VARCHAR(15) NOT NULL
);
GO

